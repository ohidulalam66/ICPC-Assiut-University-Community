// N. Numbers Histogram

#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    char S;
    cin >> S;

    int t;
    cin >> t;
    while (t--)
    {
        int N, i;
        cin >> N;
        for (i = 0; i < N; i++)
        {
            cout << S;
        }
        cout << "\n";
    }

    return 0;
}